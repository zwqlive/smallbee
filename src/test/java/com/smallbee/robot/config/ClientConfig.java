package com.smallbee.robot.config;

/**
 * 客户端配置
 * 
 * @author will
 *
 */
public class ClientConfig {
	private String host;
	private int port;
	public String getHost() {
		return host;
	}
	public void setHost(String host) {
		this.host = host;
	}
	public int getPort() {
		return port;
	}
	public void setPort(int port) {
		this.port = port;
	}
	
}
